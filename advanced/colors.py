"""
This file defines color constants in BGR (Blue, Green, Red) format
for use with the OpenCV library.
"""

# --- Primary Colors ---
# NOTE: OpenCV uses BGR order, not RGB.
RED = (0, 0, 255)
GREEN = (0, 255, 0)
BLUE = (255, 0, 0)

# --- Secondary Colors ---
YELLOW = (0, 255, 255)
CYAN = (255, 255, 0)
MAGENTA = (255, 0, 255)

# --- Basic Shades ---
WHITE = (255, 255, 255)
BLACK = (0, 0, 0)
GRAY = (128, 128, 128)

# --- Other Common Colors ---
ORANGE = (0, 165, 255)
PURPLE = (128, 0, 128)
PINK = (203, 192, 255)